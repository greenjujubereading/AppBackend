package com.qingzaoreading.pojo.po;

public class Lecturer {
    private Long lecturer_id;

    private String lecturer_name;

    private String lecturer_head_portrait_url;

    private String lecturer_introduction;

	public Long getLecturer_id() {
		return lecturer_id;
	}

	public void setLecturer_id(Long lecturer_id) {
		this.lecturer_id = lecturer_id;
	}

	public String getLecturer_name() {
		return lecturer_name;
	}

	public void setLecturer_name(String lecturer_name) {
		this.lecturer_name = lecturer_name;
	}

	public String getLecturer_head_portrait_url() {
		return lecturer_head_portrait_url;
	}

	public void setLecturer_head_portrait_url(String lecturer_head_portrait_url) {
		this.lecturer_head_portrait_url = lecturer_head_portrait_url;
	}

	public String getLecturer_introduction() {
		return lecturer_introduction;
	}

	public void setLecturer_introduction(String lecturer_introduction) {
		this.lecturer_introduction = lecturer_introduction;
	}

	 
    
    
}